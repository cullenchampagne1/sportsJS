/**
 * Get a random browser configuration
 * @returns {Object} Browser config with userAgent and headers
 */
export function getRandomBrowserConfig() {
    const config = BROWSER_CONFIGS[Math.floor(Math.random() * BROWSER_CONFIGS.length)];
    return config;
}

/**
 * Get browser config with additional custom headers
 * @param {Object} customHeaders - Additional headers to merge
 * @returns {Object} Browser config with merged headers
 */
export function getBrowserConfigWithHeaders(customHeaders = {}) {
    const config = getRandomBrowserConfig();
    return {
        ...config,
        headers: {
            ...config.headers,
            ...customHeaders
        }
    };
}

/**
 * Get all available browser configurations
 * @returns {Array} Array of all browser configs
 */
export function getAllBrowserConfigs() {
    return BROWSER_CONFIGS;
}